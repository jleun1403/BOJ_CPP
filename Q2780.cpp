int d[11][1001]; // [i번째 자리에서 시작][길이가 j]인 경우의 수 
int main(){
    for(int i=1; i<=10; i++){
        d[i][1] = 1;
    }
    for(int i=2; i<=1000; i++){
        d[1][i] = (d[2][i-1] %1234567 + d[4][i-1] %1234567) % 1234567 ;
        d[2][i] = (d[1][i-1] %1234567 + d[3][i-1] %1234567 + d[5][i-1]%1234567) %1234567;
        d[3][i] = (d[2][i-1] %1234567 + d[6][i-1] %1234567) % 1234567;
        d[4][i] = (d[1][i-1] %1234567 + d[5][i-1] %1234567 + d[7][i-1] %1234567) %1234567;
        d[5][i] = (d[2][i-1] %1234567 + d[4][i-1] %1234567 + d[6][i-1] %1234567+ d[8][i-1]%1234567)% 1234567;
        d[6][i] = (d[3][i-1] %1234567 + d[5][i-1] %1234567 + d[9][i-1] %1234567) %1234567;
        d[7][i] = (d[4][i-1] %1234567 + d[8][i-1] %1234567 + d[10][i-1] %1234567) %1234567;
        d[8][i] = (d[5][i-1] %1234567 + d[7][i-1] %1234567 + d[9][i-1] %1234567) %1234567;
        d[9][i] = (d[6][i-1] %1234567 + d[8][i-1] %1234567) %1234567;
        d[10][i]= (d[7][i-1] %1234567) %1234567;
    }
    int t, n;
    cin >> t;
    while(t--){
        cin >> n;
        int sum = 0;
        for(int i=1; i<=10; i++){
            sum += d[i][n];
        }
        cout << sum %1234567 << '\n';
    }
    return 0;
}